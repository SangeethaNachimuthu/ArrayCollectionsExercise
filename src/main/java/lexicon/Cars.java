package lexicon;

import java.util.HashMap;

/*
This class contains the car fields of
id, brand and model.
 */

public class Cars {
    Integer id;
    String brand;
    String model;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }
}
